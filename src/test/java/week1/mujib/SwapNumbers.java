package week1.mujib;

public class SwapNumbers {
    public static void main(String[] args) {

        int x = 74;
        int y = 25;

        // X = X + Y
        x = x + y  ;

        // Y = X - Y

        y = x - y ;

        // X = X - Y

        x = x - y ;

        System.out.println("x = " + x);
        System.out.println("y = " + y);


    }

}
